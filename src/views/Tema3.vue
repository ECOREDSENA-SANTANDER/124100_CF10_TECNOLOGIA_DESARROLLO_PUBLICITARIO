        p.mb-0 Una de las partes más importantes y decisivas a la hora de crear un nuevo proyecto de diseño gráfico es el estudio de la composición. Esto es, la disposición de los distintos elementos dentro del espacio visual, de manera equilibrada y ordenada, con el fin de transmitir un mensaje al público objetivo. El éxito de un proyecto gráfico reside fundamentalmente en una composición perfecta que sea capaz de transmitir una idea de manera sencilla y directa. Hay que tener claro desde un primer momento qué elementos se van a incluir, y de qué manera se van a combinar para crear una estructura compositiva armónica. Dentro de la composición entran en juego aspectos como el tamaño, las texturas, los colores, los pesos, etc., cada uno de ellos debe estar en concordancia y dispuestos de manera equilibrada. (Estudio mique, s.f.)
    
